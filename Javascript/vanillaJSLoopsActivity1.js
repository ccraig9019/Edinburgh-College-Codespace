let n = 15 //declaring variable that will act as a limit

for (let i=0; i <= n; ++i) { //declaring variable that will act as an iterator
		if (i % 2) { //checking to see if the iterator's value is odd or even by checking whether it creates a remainder
		console.log(i + " is even")
		}
		else {
			console.log(i + " is odd")
		}
}
